void UMXRunReportUI::DisplayReport(const FMXRunReport& Report)
{
    CachedReport = Report;

    ShowHeader(Report.run_data);
    PlayOpeningNarrative(Report.opening_narrative);
    ShowNumbers(Report.run_data);
    ShowHatCensus(Report.hat_census, TArray<FString>());  // Commentary generated by DEMS; pass empty if none
    ShowLevelBreakdown(Report.run_data);
    ShowHighlights(Report.highlights);
    ShowAwards(Report.awards);
    ShowDeathRoll(Report.death_roll);
    ShowSacrificeHonorRoll(Report.sacrifice_honor_roll);
    PlayClosingNarrative(Report.closing_narrative);

    // Build section tab bar
    if (SectionTabBar && WidgetTree)
    {
        SectionTabBar->ClearChildren();
        const TArray<FString> TabLabels = {
            TEXT("Header"), TEXT("Opening"), TEXT("Numbers"), TEXT("Hats"),
            TEXT("Levels"), TEXT("Highlights"), TEXT("Awards"),
            TEXT("Deaths"), TEXT("Sacrifices"), TEXT("Closing")
        };
        for (int32 i = 0; i < TabLabels.Num(); ++i)
        {
            UWidget* Tab = BuildTabButton(i, TabLabels[i]);
            if (Tab) SectionTabBar->AddChild(Tab);
        }
    }

    NavigateToSection(MXReportSection::Opening);
}

