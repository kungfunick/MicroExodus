// Section: Hat Census
// ---------------------------------------------------------------------------

void UMXRunReportUI::ShowHatCensus(const FMXHatCensus& Census, const TArray<FString>& Commentary)
{
    if (!HatCensusPanel || !WidgetTree) return;

    HatCensusPanel->ClearChildren();

    auto AddHatStat = [&](const FString& Label, const FString& Value)
    {
        UWidget* Row = BuildStatRow(Label, Value);
        if (Row) HatCensusPanel->AddChild(Row);
    };

    AddHatStat(TEXT("Hatted Robots"),   FString::FromInt(Census.total_hatted));
    AddHatStat(TEXT("Unique Hats"),     FString::FromInt(Census.unique_hats));
    AddHatStat(TEXT("Most Common Hat"), Census.most_common_hat.IsEmpty() ? TEXT("—") : Census.most_common_hat);
    AddHatStat(TEXT("Rarest Hat"),      Census.rarest_hat.IsEmpty() ? TEXT("—") : Census.rarest_hat);
    AddHatStat(TEXT("Hats Lost"),       FString::FromInt(Census.hats_lost));
    AddHatStat(TEXT("Hat Survival"),
        FString::Printf(TEXT("%.0f%%"), Census.hat_survival_rate * 100.0f));

    // Interleave commentary lines
    for (const FString& Line : Commentary)
    {
        UTextBlock* CommentText = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
        if (CommentText)
        {
            CommentText->SetText(FText::FromString(Line));
            CommentText->SetColorAndOpacity(FSlateColor(FLinearColor(0.7f, 0.7f, 0.8f, 1.0f)));
            HatCensusPanel->AddChild(CommentText);
        }
    }
}

// ---------------------------------------------------------------------------
// Section: Level Breakdown
// ---------------------------------------------------------------------------

void UMXRunReportUI::ShowLevelBreakdown(const FMXRunData& RunData)
{
    if (!LevelBreakdownPanel || !WidgetTree) return;

    LevelBreakdownPanel->ClearChildren();
    ExpandedLevels.Empty();

    const int32 MaxLevel = FMath::Max(1, RunData.levels_completed);
    for (int32 L = 1; L <= MaxLevel; ++L)
    {
        TArray<FMXEventData> LevelEvents = GetLevelEvents(RunData, L);
        UWidget* Card = BuildLevelCard(L, LevelEvents);
        if (Card) LevelBreakdownPanel->AddChild(Card);
    }
}

void UMXRunReportUI::ToggleLevelExpanded(int32 LevelNumber)
{
    if (ExpandedLevels.Contains(LevelNumber))
    {
        ExpandedLevels.Remove(LevelNumber);
    }
    else
    {
        ExpandedLevels.Add(LevelNumber);
    }
    // Re-build level breakdown to reflect expansion state
    ShowLevelBreakdown(CachedReport.run_data);
}

// ---------------------------------------------------------------------------
// Section: Highlights
// ---------------------------------------------------------------------------

void UMXRunReportUI::ShowHighlights(const TArray<FMXHighlight>& Highlights)
{
    if (!HighlightsPanel || !WidgetTree) return;

    HighlightsPanel->ClearChildren();

    for (int32 i = 0; i < Highlights.Num(); ++i)
    {
        UWidget* Card = BuildHighlightCard(Highlights[i]);
        if (Card)
        {
            HighlightsPanel->AddChild(Card);
            OnHighlightRevealed(Highlights[i], i);
        }
    }
}

// ---------------------------------------------------------------------------
// Section: Awards (sequential reveal)
// ---------------------------------------------------------------------------

void UMXRunReportUI::ShowAwards(const TArray<FMXAward>& Awards)
{
    if (!AwardsPanel) return;

    AwardsPanel->ClearChildren();
    PendingAwards = Awards;
    AwardRevealIndex = 0;

    if (PendingAwards.IsEmpty()) return;

    // Reveal first immediately, subsequent ones on timer
    RevealNextAward();
}

void UMXRunReportUI::RevealNextAward()
{
    if (!AwardsPanel || AwardRevealIndex >= PendingAwards.Num()) return;

    UWidget* Card = BuildAwardCard(PendingAwards[AwardRevealIndex]);
    if (Card)
    {
        AwardsPanel->AddChild(Card);
        OnAwardRevealed(PendingAwards[AwardRevealIndex], AwardRevealIndex);
    }

    ++AwardRevealIndex;

    if (AwardRevealIndex < PendingAwards.Num() && GetWorld())
    {
        GetWorld()->GetTimerManager().SetTimer(
            AwardRevealTimer, this, &UMXRunReportUI::RevealNextAward,
            AwardRevealInterval, false);
    }
}

