void UMXRunReportUI::ShowHeader(const FMXRunData& RunData)
{
    if (RunNumberText)
    {
        RunNumberText->SetText(FText::FromString(
            FString::Printf(TEXT("Run #%d"), RunData.run_number)));
    }

    if (OutcomeText)
    {
        const bool bSuccess = (RunData.outcome == ERunOutcome::Success);
        OutcomeText->SetText(FText::FromString(bSuccess ? TEXT("SURVIVED") : TEXT("FAILED")));
        OutcomeText->SetColorAndOpacity(FSlateColor(bSuccess
            ? FLinearColor(0.1f, 0.9f, 0.2f, 1.0f)
            : FLinearColor(0.9f, 0.1f, 0.1f, 1.0f)));
    }

    if (DurationText && RunData.end_time > RunData.start_time)
    {
        const float DurationSecs = (float)(RunData.end_time - RunData.start_time).GetTotalSeconds();
        DurationText->SetText(FText::FromString(FormatDuration(DurationSecs)));
    }

    if (HeaderPanel && WidgetTree)
    {
        // Tier + modifiers row
        const UEnum* TierEnum = StaticEnum<ETier>();
        const FString TierStr = TierEnum
            ? TierEnum->GetNameStringByValue((int64)RunData.tier)
            : TEXT("Standard");

        UTextBlock* TierText = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
        if (TierText)
        {
            TierText->SetText(FText::FromString(FString::Printf(TEXT("Tier: %s"), *TierStr)));
            HeaderPanel->AddChild(TierText);
        }

        if (!RunData.active_modifiers.IsEmpty())
        {
            UTextBlock* ModText = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
            if (ModText)
            {
                const FString ModStr = FString::Join(RunData.active_modifiers, TEXT(", "));
                ModText->SetText(FText::FromString(FString::Printf(TEXT("Modifiers: %s"), *ModStr)));
                HeaderPanel->AddChild(ModText);
            }
        }
    }
}

// ---------------------------------------------------------------------------
// Section: Opening Narrative (typewriter)
// ---------------------------------------------------------------------------

void UMXRunReportUI::PlayOpeningNarrative(const FString& Text)
{
    if (!OpeningNarrativeText || Text.IsEmpty()) return;

    TypewriterFullText    = Text;
    TypewriterCharIndex   = 0;
    bTypewriterClosing    = false;

    OpeningNarrativeText->SetText(FText::FromString(TEXT("")));

    const float TickInterval = (TypewriterCPS > 0.0f) ? (1.0f / TypewriterCPS) : 0.025f;

    if (GetWorld())
    {
        GetWorld()->GetTimerManager().SetTimer(
            TypewriterTimer,
            this, &UMXRunReportUI::TypewriterTick,
            TickInterval, true);
    }
}

void UMXRunReportUI::TypewriterTick()
{
    if (!OpeningNarrativeText) return;

    if (TypewriterCharIndex >= TypewriterFullText.Len())
    {
        if (GetWorld()) GetWorld()->GetTimerManager().ClearTimer(TypewriterTimer);

        if (bTypewriterClosing)
        {
            // Show continue button after delay
            if (GetWorld())
            {
                FTimerHandle DelayHandle;
                GetWorld()->GetTimerManager().SetTimer(DelayHandle, [this]()
                {
                    if (ContinueButton)
                    {
                        ContinueButton->SetVisibility(ESlateVisibility::Visible);
                    }
                    OnClosingNarrativeComplete();
                }, ContinueDelay, false);
            }
        }
        else
        {
            OnOpeningNarrativeComplete();
        }
        return;
    }

    ++TypewriterCharIndex;
    const FString Current = TypewriterFullText.Left(TypewriterCharIndex);
    OpeningNarrativeText->SetText(FText::FromString(Current));
}

