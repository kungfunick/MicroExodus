TArray<FMXEventData> UMXRunReportUI::GetLevelEvents(const FMXRunData& RunData, int32 LevelNumber) const
{
    TArray<FMXEventData> Result;
    for (const FMXEventData& Ev : RunData.events)
    {
        if (Ev.level_number == LevelNumber)
        {
            Result.Add(Ev);
        }
    }
    return Result;
}

FString UMXRunReportUI::FormatDuration(float Seconds)
{
    const int32 Total   = FMath::FloorToInt(Seconds);
    const int32 Minutes = Total / 60;
    const int32 Secs    = Total % 60;
    return FString::Printf(TEXT("%dm %ds"), Minutes, Secs);
}

