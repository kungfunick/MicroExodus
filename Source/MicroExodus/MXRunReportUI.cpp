UWidget* UMXRunReportUI::BuildAwardCard(const FMXAward& Award)
{
    if (!WidgetTree) return nullptr;

    UBorder* Card = WidgetTree->ConstructWidget<UBorder>(UBorder::StaticClass());
    if (!Card) return nullptr;

    Card->SetBrushColor(FLinearColor(0.15f, 0.12f, 0.05f, 1.0f));

    UVerticalBox* Inner = WidgetTree->ConstructWidget<UVerticalBox>(UVerticalBox::StaticClass());
    if (Inner)
    {
        const UEnum* CatEnum = StaticEnum<EAwardCategory>();
        const FString CatStr = CatEnum
            ? CatEnum->GetNameStringByValue((int64)Award.category)
            : TEXT("Award");

        auto AddLine = [&](const FString& Text, FLinearColor Color = FLinearColor::White)
        {
            UTextBlock* T = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
            if (T)
            {
                T->SetText(FText::FromString(Text));
                T->SetColorAndOpacity(FSlateColor(Color));
                Inner->AddChild(T);
            }
        };

        AddLine(CatStr, FLinearColor(1.0f, 0.85f, 0.3f, 1.0f));
        AddLine(Award.robot_name);
        if (!Award.stat_value.IsEmpty()) AddLine(Award.stat_value, FLinearColor(0.7f, 0.7f, 0.7f, 1.0f));
        if (!Award.narrative.IsEmpty())  AddLine(Award.narrative,  FLinearColor(0.85f, 0.85f, 1.0f, 1.0f));

        Card->SetContent(Inner);
    }

    return Card;
}

