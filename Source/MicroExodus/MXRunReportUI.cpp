void UMXRunReportUI::NativeConstruct()
{
    Super::NativeConstruct();

    if (ContinueButton)
    {
        ContinueButton->SetVisibility(ESlateVisibility::Collapsed);
        ContinueButton->OnClicked.AddDynamic(this, &UMXRunReportUI::OnContinueClicked);
    }

    if (ShareButton)
    {
        ShareButton->OnClicked.AddDynamic(this, &UMXRunReportUI::OnShareClicked);
    }

    if (FullLogButton)
    {
        FullLogButton->OnClicked.AddDynamic(this, &UMXRunReportUI::OnFullLogClicked);
    }

    if (FullEventLogScrollBox)
    {
        FullEventLogScrollBox->SetVisibility(ESlateVisibility::Collapsed);
    }
}

void UMXRunReportUI::NativeDestruct()
{
    if (GetWorld())
    {
        GetWorld()->GetTimerManager().ClearTimer(TypewriterTimer);
        GetWorld()->GetTimerManager().ClearTimer(StatCounterTimer);
        GetWorld()->GetTimerManager().ClearTimer(AwardRevealTimer);
        GetWorld()->GetTimerManager().ClearTimer(DeathRevealTimer);
        GetWorld()->GetTimerManager().ClearTimer(SacrificeRevealTimer);
    }
    Super::NativeDestruct();
}

