void UMXRunReportUI::NavigateToSection(int32 SectionIndex)
{
    if (!SectionSwitcher) return;

    const int32 Clamped = FMath::Clamp(SectionIndex, 0, MXReportSection::Count - 1);
    SectionSwitcher->SetActiveWidgetIndex(Clamped);
}

void UMXRunReportUI::ShowFullEventLog(const TArray<FMXEventData>& Events)
{
    if (!FullEventLogScrollBox || !WidgetTree) return;

    FullEventLogScrollBox->ClearChildren();

    for (const FMXEventData& Ev : Events)
    {
        UTextBlock* Line = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
        if (Line)
        {
            Line->SetText(FText::FromString(FString::Printf(
                TEXT("[L%d] %s"), Ev.level_number, *Ev.message_text)));
            FullEventLogScrollBox->AddChild(Line);
        }
    }

    const bool bVisible = (FullEventLogScrollBox->GetVisibility() == ESlateVisibility::Collapsed);
    FullEventLogScrollBox->SetVisibility(bVisible
        ? ESlateVisibility::SelfHitTestInvisible
        : ESlateVisibility::Collapsed);
}

