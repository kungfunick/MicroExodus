void UMXRunReportUI::ShowNumbers(const FMXRunData& RunData)
{
    if (!NumbersPanel || !WidgetTree) return;

    NumbersPanel->ClearChildren();
    StatCounters.Empty();

    auto AddStat = [&](const FString& Label, int32 Value)
    {
        UTextBlock* StatText = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
        if (!StatText) return;

        StatText->SetText(FText::FromString(FString::Printf(TEXT("%s: 0"), *Label)));
        NumbersPanel->AddChild(StatText);

        FStatCounter Counter;
        Counter.Label   = Label;
        Counter.Current = 0;
        Counter.Target  = Value;
        Counter.Widget  = StatText;
        StatCounters.Add(Counter);
    };

    AddStat(TEXT("Robots Deployed"),  RunData.robots_deployed);
    AddStat(TEXT("Survived"),         RunData.robots_survived);
    AddStat(TEXT("Lost"),             RunData.robots_lost);
    AddStat(TEXT("Rescued"),          RunData.robots_rescued);
    AddStat(TEXT("Levels Completed"), RunData.levels_completed);
    AddStat(TEXT("Final Score"),      RunData.score);

    if (GetWorld() && !StatCounters.IsEmpty())
    {
        GetWorld()->GetTimerManager().SetTimer(
            StatCounterTimer,
            this, &UMXRunReportUI::StatCounterTick,
            StatTickInterval, true);
    }
}

void UMXRunReportUI::StatCounterTick()
{
    bool bAllDone = true;

    for (FStatCounter& Counter : StatCounters)
    {
        if (Counter.Current >= Counter.Target) continue;

        bAllDone = false;
        const int32 Step = FMath::Max(1, (Counter.Target - Counter.Current) / 8);
        Counter.Current   = FMath::Min(Counter.Current + Step, Counter.Target);

        if (Counter.Widget)
        {
            Counter.Widget->SetText(FText::FromString(
                FString::Printf(TEXT("%s: %d"), *Counter.Label, Counter.Current)));
        }

        OnStatCounterTick(Counter.Label, Counter.Current, Counter.Target);
    }

    if (bAllDone && GetWorld())
    {
        GetWorld()->GetTimerManager().ClearTimer(StatCounterTimer);
    }
}

