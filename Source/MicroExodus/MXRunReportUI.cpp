UWidget* UMXRunReportUI::BuildLevelCard(int32 LevelNumber, const TArray<FMXEventData>& LevelEvents)
{
    if (!WidgetTree) return nullptr;

    UBorder* Card = WidgetTree->ConstructWidget<UBorder>(UBorder::StaticClass());
    if (!Card) return nullptr;

    Card->SetBrushColor(FLinearColor(0.1f, 0.1f, 0.14f, 1.0f));

    UVerticalBox* Inner = WidgetTree->ConstructWidget<UVerticalBox>(UVerticalBox::StaticClass());
    if (Inner)
    {
        // Header row
        UTextBlock* Header = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
        if (Header)
        {
            Header->SetText(FText::FromString(
                FString::Printf(TEXT("Level %d  (%d events)"), LevelNumber, LevelEvents.Num())));
            Inner->AddChild(Header);
        }

        // If expanded, show event summaries
        if (ExpandedLevels.Contains(LevelNumber))
        {
            for (const FMXEventData& Ev : LevelEvents)
            {
                UTextBlock* EvText = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
                if (EvText)
                {
                    EvText->SetText(FText::FromString(FString::Printf(TEXT("  • %s"), *Ev.message_text)));
                    EvText->SetColorAndOpacity(FSlateColor(FLinearColor(0.7f, 0.7f, 0.7f, 1.0f)));
                    Inner->AddChild(EvText);
                }
            }
        }

        Card->SetContent(Inner);
    }

    return Card;
}

UWidget* UMXRunReportUI::BuildStatRow(const FString& Label, const FString& Value)
{
    if (!WidgetTree) return nullptr;

    UHorizontalBox* Row = WidgetTree->ConstructWidget<UHorizontalBox>(UHorizontalBox::StaticClass());
    if (!Row) return nullptr;

    UTextBlock* LabelText = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
    if (LabelText)
    {
        LabelText->SetText(FText::FromString(Label + TEXT(":")));
        Row->AddChild(LabelText);
    }

    UTextBlock* ValueText = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
    if (ValueText)
    {
        ValueText->SetText(FText::FromString(Value));
        Row->AddChild(ValueText);
    }

    return Row;
}

UWidget* UMXRunReportUI::BuildTabButton(int32 SectionIndex, const FString& Label)
{
    if (!WidgetTree) return nullptr;

    UButton* TabBtn = WidgetTree->ConstructWidget<UButton>(UButton::StaticClass());
    if (!TabBtn) return nullptr;

    UTextBlock* BtnText = WidgetTree->ConstructWidget<UTextBlock>(UTextBlock::StaticClass());
    if (BtnText)
    {
        BtnText->SetText(FText::FromString(Label));
        TabBtn->SetContent(BtnText);
    }

    // Bind navigation — capture SectionIndex by value
    TabBtn->OnClicked.AddDynamic(this, &UMXRunReportUI::OnContinueClicked); // placeholder; BP wires real index

    return TabBtn;
}

