// ---------------------------------------------------------------------------
// Section: Closing Narrative
// ---------------------------------------------------------------------------

void UMXRunReportUI::PlayClosingNarrative(const FString& Text)
{
    if (!ClosingNarrativeText || Text.IsEmpty()) return;

    bTypewriterClosing  = true;
    TypewriterFullText  = Text;
    TypewriterCharIndex = 0;

    ClosingNarrativeText->SetText(FText::FromString(TEXT("")));

    const float TickInterval = (TypewriterCPS > 0.0f) ? (1.0f / TypewriterCPS) : 0.025f;

    if (GetWorld())
    {
        // Clear any running opening typewriter
        GetWorld()->GetTimerManager().ClearTimer(TypewriterTimer);
        // Reuse same timer but now routing to ClosingNarrativeText
        GetWorld()->GetTimerManager().SetTimer(
            TypewriterTimer, this, &UMXRunReportUI::TypewriterTick,
            TickInterval, true);
    }
}

